package chap14_objectarray.car;

public class CarStat {
	public String company;
	public String model;
	public int price;
	public String color;
	
	public CarStat()
	{
		
	}
	
	public CarStat(String company, String model, int price, String color)
	{
		this.company = company;
		this.model = model;
		this.price = price;
		this.color = color;
		
	}

}
